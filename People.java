
public class People {

	private String name;
	private double salary;
	private int authorization;
	
	public String getName() {return name;}															// get People object's name
	public void setName(String newName) {this.name = newName;}										// set People object's name
	public double getSalary() {return salary;}														// get People object's salary
	public void setSalary(double newSalary) {this.salary = newSalary;}								// set People object's salary
	public int getAuthorization() {return authorization; }											// get People object's authorization
	public void setAuthorization(int newAuthorization) {this.authorization = newAuthorization; }	// set People object's authorization

}
